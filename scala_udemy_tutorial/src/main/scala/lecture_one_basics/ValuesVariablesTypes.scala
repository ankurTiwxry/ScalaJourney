package lecture_one_basics

object ValuesVariablesTypes extends App{

    val x: Int = 42;
    println(x);

    // VALs are immutable: Preferred
    val a_string: String = "hello";
    val a_bool: Boolean = false;
    val a_char: Char = 'a';
    val a_short: Short = 4523;
    val a_long: Long = 1234523;
    val a_float: Float = 0.0f;
    val a_double: Double = 0.0;

    // VARs are mutable
    var a_variable: Int = 4;

}
